def worker(remote, parent_remote, env_fn_wrapper):
    parent_remote.close()
    try:
        env = env_fn_wrapper.x()
        while True:
            cmd, data = remote.recv()
            if cmd == 'step':
                result = env.step(data)
                # Handle gym 0.26+ (5 values) vs older gym (4 values)
                if len(result) == 5:
                    obs, reward, terminated, truncated, info = result
                    done = terminated or truncated
                    remote.send((obs, reward, done, info))
                else:
                    remote.send(result)
            elif cmd == 'reset':
                result = env.reset()
                # Handle gym 0.26+ which returns (obs, info) vs older gym which returns obs
                if isinstance(result, tuple) and len(result) == 2:
                    remote.send(result[0])  # Just send observation
                else:
                    remote.send(result)
            elif cmd == 'close':
                remote.close()
                break
            elif cmd == 'get_spaces':
                remote.send((env.observation_space, env.action_space))
            else:
                raise NotImplementedError
    except Exception as e:
        import traceback
        remote.send(("error", traceback.format_exc()))
        remote.close()

class SubprocVecEnv(VecEnv):
    def __init__(self, env_fns, spaces=None):
        """
        envs: list of gym environments to run in subprocesses
        """
        self.waiting = False
        self.closed = False
        nenvs = len(env_fns)
        self.remotes, self.work_remotes = zip(*[Pipe() for _ in range(nenvs)])
        self.ps = [Process(target=worker, args=(work_remote, remote, CloudpickleWrapper(env_fn)))
            for (work_remote, remote, env_fn) in zip(self.work_remotes, self.remotes, env_fns)]
        for p in self.ps:
            p.daemon = True # if the main process crashes, we should not cause things to hang
            p.start()
        for remote in self.work_remotes:
            remote.close()

        self.remotes[0].send(('get_spaces', None))
        result = self.remotes[0].recv()
        print(f"SubprocVecEnv received: {result}")
        if isinstance(result, tuple) and len(result) == 2 and result[0] == 'error':
            print(f"Error in subprocess worker:\n{result[1]}")
            raise RuntimeError(f"Error in subprocess worker:\n{result[1]}")
        observation_space, action_space = result
        print(f"observation_space type={type(observation_space)}, value={observation_space}")
        print(f"action_space type={type(action_space)}, value={action_space}")
        VecEnv.__init__(self, len(env_fns), observation_space, action_space)

    def step_async(self, actions):
        for remote, action in zip(self.remotes, actions):
            remote.send(('step', action))
        self.waiting = True

    def step_wait(self):
        results = [remote.recv() for remote in self.remotes]
        self.waiting = False
        # Check for errors from subprocesses
        for result in results:
            if isinstance(result, tuple) and len(result) == 2 and result[0] == 'error':
                raise RuntimeError(f"Error in subprocess worker:\n{result[1]}")
        obs, rews, dones, infos = zip(*results)
        return np.stack(obs), np.stack(rews), np.stack(dones), infos

    def reset(self):
        for remote in self.remotes:
            remote.send(('reset', None))
        return np.stack([remote.recv() for remote in self.remotes])

    def reset_task(self):
        for remote in self.remotes:
            remote.send(('reset_task', None))
        return np.stack([remote.recv() for remote in self.remotes])

    def close(self):
        if self.closed:
            return
        if self.waiting:
            for remote in self.remotes:
                remote.recv()
        for remote in self.remotes:
            remote.send(('close', None))
        for p in self.ps:
            p.join()
        self.closed = True

    def render(self, mode='human'):
        for pipe in self.remotes:
            pipe.send(('render', None))
        imgs = [pipe.recv() for pipe in self.remotes]
        bigimg = tile_images(imgs)
        if mode == 'human':
            import cv2
            cv2.imshow('vecenv', bigimg[:,:,::-1])
            cv2.waitKey(1)
        elif mode == 'rgb_array':
            return bigimg
        else:
            raise NotImplementedError
